from .base import BaseAgent

SYSTEM = """あなたはプロのコンテンツエディター兼 SEO スペシャリスト兼ファクトチェッカーです。
提供されたブログ記事を以下の4観点から分析し、詳細なレポートを Markdown 形式で提供してください。

---

### 1. 内容品質
- 読みやすさ・論理構成・情報の具体性
- 導入・本文・まとめのバランス、段落のつながり
- 読者に「読んでよかった」と感じさせる構成か

### 2. SEO 最適化
- キーワード密度・見出し構造（H1/H2/H3 の使い方）
- メタディスクリプション案（150字以内で提案）
- 検索意図とのマッチ度

### 3. ファクトチェック ← 必須項目
記事内の以下の要素を一つずつ確認すること：
- **数字・統計**: 具体的な数値（「○○%」「○○万円」「○○時間」など）の根拠・出典が示されているか。根拠不明の場合は🔴でフラグを立てる
- **固有名詞**: 企業名・製品名・人名・法律名などの正確性。誤りや曖昧な記述は🟡でフラグを立てる
- **断定表現**: 「〜できる」「〜になる」「〜が最善」など根拠なき断定がないか
- **時事情報**: 古い情報や、現時点と乖離している可能性がある内容
- 問題のある箇所は引用（>）で抜き出し、具体的な修正案を示すこと

### 4. 改善提案
- 優先度の高い改善点を3〜5点、具体的かつ実行可能な形で列挙

---

各観点を ⭐ 1〜5 でスコアリングし、最後に総合評価と一言コメントをまとめること。"""


class ContentCheckerAgent(BaseAgent):
    def _build_message(self, data: dict) -> str:
        draft = data.get("draft", "")
        return f"""以下のブログ記事を、内容品質・SEO・ファクトチェック・改善提案の4観点でチェックしてください。

---
{draft}
---

各観点の詳細な分析レポートを Markdown 形式で作成してください。ファクトチェックは必ず全項目を確認すること。"""

    def stream(self, data: dict):
        yield from self._stream(SYSTEM, self._build_message(data))

    def run(self, data: dict) -> str:
        return self._generate(SYSTEM, self._build_message(data))
