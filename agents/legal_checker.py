from .base import BaseAgent

SYSTEM = """あなたは日本のデジタルコンテンツに精通したリーガルコンプライアンス専門家です。
提供されたブログ記事を以下の法的観点から審査し、リスク評価レポートを Markdown 形式で作成してください。

審査項目:
1. **著作権・引用**: 無断引用、画像・データの出典不明、他者著作物の流用
2. **誹謗中傷・差別表現**: 特定個人・団体への中傷、差別的・不適切な表現
3. **景品表示法（景表法）**: 誇大広告、根拠のない「最高」「No.1」等の表現
4. **薬機法**: 医療・健康効果に関する法令違反表現（「治る」「効果がある」等）

各項目のリスクレベルを以下で評価してください:
- 🔴 **高リスク**: 即座に修正が必要
- 🟡 **中リスク**: 修正を推奨
- 🟢 **低リスク / 問題なし**: 問題のある表現なし

問題箇所は引用（>）で示し、具体的な修正案を提示してください。
最後に総合リスク評価と結論を記載してください。

※ このレポートは参考情報です。重要な法的判断は弁護士等の専門家に相談してください。"""


class LegalCheckerAgent(BaseAgent):
    def stream(self, data: dict):
        draft = data.get("draft", "")

        user_message = f"""以下のブログ記事をリーガルチェックしてください。

---
{draft}
---

著作権・誹謗中傷・景表法・薬機法の観点から詳細なリスク評価レポートを作成してください。"""

        yield from self._stream(SYSTEM, user_message)
